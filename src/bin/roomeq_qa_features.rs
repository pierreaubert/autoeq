use anyhow::{Context, Result};
use autoeq::roomeq::load_config;

#[path = "roomeq_qa_features/consts.rs"]
mod consts;
#[path = "roomeq_qa_features/misc.rs"]
mod misc;
#[path = "roomeq_qa_features/run.rs"]
mod run;
#[path = "roomeq_qa_features/step_result.rs"]
mod step_result;
#[path = "roomeq_qa_features/types.rs"]
mod types;

use misc::discover_recordings;
use misc::find_project_root;
use run::run_pass;
use step_result::print_pass_results;
use step_result::validate_pass;

fn main() -> Result<()> {
    let project_root = find_project_root()?;
    let recordings = discover_recordings(&project_root)?;

    println!(
        "RoomEQ Feature Progression QA — {} recording(s)\n",
        recordings.len()
    );

    let mut all_errors: Vec<String> = Vec::new();
    let mut pass_count = 0;
    let mut fail_count = 0;

    for (name, config_path) in &recordings {
        println!("=== Recording: {} ===", name);

        let (base_config, _config_dir) = load_config(config_path, None)
            .with_context(|| format!("Failed to load {}", config_path.display()))?;

        let mut recording_ok = true;

        // --- Pass A: Flat target ---
        println!("--- Pass A: Flat target ---");
        let pass_a = run_pass(name, &base_config, false)?;
        print_pass_results(&pass_a);
        let errors_a = validate_pass("Pass A", &pass_a, true);
        if errors_a.is_empty() {
            println!("  => PASS");
        } else {
            println!("  => FAIL");
            recording_ok = false;
            for e in &errors_a {
                println!("{}", e);
            }
            all_errors.extend(errors_a);
        }

        // --- Pass B: Harman tilt ---
        println!("--- Pass B: Harman tilt ---");
        let pass_b = run_pass(name, &base_config, true)?;
        print_pass_results(&pass_b);
        let errors_b = validate_pass("Pass B", &pass_b, false);
        if errors_b.is_empty() {
            println!("  => PASS");
        } else {
            println!("  => FAIL");
            recording_ok = false;
            for e in &errors_b {
                println!("{}", e);
            }
            all_errors.extend(errors_b);
        }

        if recording_ok {
            pass_count += 1;
        } else {
            fail_count += 1;
        }

        println!();
    }

    // Summary
    let total = pass_count + fail_count;
    if all_errors.is_empty() {
        println!("=== Summary: {}/{} recordings PASS ===", pass_count, total);
        Ok(())
    } else {
        println!("=== Summary: {}/{} recordings FAIL ===", fail_count, total);
        for e in &all_errors {
            eprintln!("{}", e);
        }
        std::process::exit(1);
    }
}
