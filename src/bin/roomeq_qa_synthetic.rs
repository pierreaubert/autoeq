use anyhow::Result;
use autoeq::Curve;
use autoeq::iir::{Biquad, BiquadFilterType};
use autoeq::roomeq::ProcessingMode;
use autoeq::roomeq::synthetic::{
    generate_flat_curve, generate_harman_tilt_curve, generate_multisub_scenario, generate_scenario,
    generate_speaker_rolloff_curve,
};
use std::fmt::Write as _;
use std::time::Instant;

